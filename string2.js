// 1. Find length of string
let s1 = "HELLO";
console.log(s1.length);

// 2. Convert to uppercase
let s2 = "hello";
console.log(s2.toUpperCase());

// 3. Convert to lowercase
let s3 = "WORLD";
console.log(s3.toLowerCase());

// 4. Extract part of string
let s4 = "JAVASCRIPT";
console.log(s4.substring(0, 4));

// 5. Replace a word
let s5 = "I like Java";
console.log(s5.replace("Java", "JavaScript"));

// 6. Check if string contains a word
let s6 = "Learning JavaScript";
console.log(s6.includes("JavaScript"));

// 7. Split string into array
let s7 = "A,B,C,D";
console.log(s7.split(","));