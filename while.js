// 1. Numbers 1 to 7
let arr8 = [];
let i = 1;
while (i <= 7) {
  arr8.push(i);
  i++;
}
console.log(arr8);

// 2. Even numbers
let arr9 = [];
let j = 2;
while (j <= 14) {
  arr9.push(j);
  j += 2;
}
console.log(arr9);

// 3. Odd numbers
let arr10 = [];
let k = 1;
while (k <= 13) {
  arr10.push(k);
  k += 2;
}
console.log(arr10);

// 4. Squares
let arr11 = [];
let m = 1;
while (m <= 7) {
  arr11.push(m * m);
  m++;
}
console.log(arr11);

// 5. Reverse order
let arr12 = [];
let n = 7;
while (n > 0) {
  arr12.push(n);
  n--;
}
console.log(arr12);

// 6. Constant value
let arr13 = [];
let c = 1;
while (c <= 7) {
  arr13.push(10);
  c++;
}
console.log(arr13);

// 7. String characters
let arr14 = [];
let s = "WHILE";
let x = 0;
while (x < s.length) {
  arr14.push(s[x]);
  x++;
}
console.log(arr14);